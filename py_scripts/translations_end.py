def process_file():
    path = BASE_DIR / FILENAME
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    count = 0
    no_id = 0
    for entry in data:
        if entry.get('type') not in ('MSGSET', 'LOGSET'):
            continue
        if 'TextENGNew' in entry:
            continue
        mid = entry.get('MessageID')
        if mid is None:
            no_id += 1
            continue
        if mid in TRANSLATIONS:
            entry['TextENGNew'] = TRANSLATIONS[mid]
            count += 1
        else:
            print(f"  WARNING: No translation for MessageID {mid}")
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write('\n')
    print(f"{FILENAME}: wrote {count} translations, skipped {no_id} null-ID entries")
    return count

if __name__ == '__main__':
    process_file()
