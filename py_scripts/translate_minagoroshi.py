def process_file(filename):
    filepath = Path(__file__).resolve().parent.parent / filename
    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)

    count_translated = 0
    count_skipped = 0
    count_missing = 0

    for entry in data:
        if entry.get('type') not in ('MSGSET', 'LOGSET'):
            continue
        if 'TextJPN' not in entry:
            continue
        if 'TextENGNew' in entry:
            count_skipped += 1
            continue
        msg_id = entry.get('MessageID')
        if msg_id in TRANSLATIONS:
            entry['TextENGNew'] = TRANSLATIONS[msg_id]
            count_translated += 1
        else:
            count_missing += 1

    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    return count_translated, count_skipped, count_missing


