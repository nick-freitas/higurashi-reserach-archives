def strip_tags(text):
    """Remove game engine tags, preserve \\n."""
    # Ruby/furigana: @bREADING.@<KANJI@> -> KANJI
    text = re.sub(r'@b[^.]*\.@<([^>]*)@>', r'\1', text)
    # Voice tags
    text = re.sub(r'@v[A-Za-z0-9/_|]+\.', '', text)
    # Wait/other numeric tags
    text = re.sub(r'@w\d+\.', '', text)
    text = re.sub(r'@o\d+\.', '', text)
    # Click wait, return, pipe, misc
    text = re.sub(r'@[krb|y]', '', text)
    # Remaining @ tags
    text = re.sub(r'@[A-Za-z][A-Za-z0-9_/|]*\.?', '', text)
    return text.strip()


