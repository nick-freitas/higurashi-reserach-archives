def process_file(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)

    count_added = 0
    count_skipped = 0
    count_missing = 0

    for entry in data:
        if entry.get('type') not in ('MSGSET', 'LOGSET'):
            continue
        if 'TextENGNew' in entry:
            count_skipped += 1
            continue

        translation = translate(entry)
        if translation is not None:
            entry['TextENGNew'] = translation
            count_added += 1
        else:
            count_missing += 1

    if count_added > 0:
        with open(filepath, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)

    return count_added, count_skipped, count_missing


