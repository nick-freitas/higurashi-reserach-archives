def clean_text(text):
    """Strip game tags and backtick markers from Japanese text display."""
    if not text:
        return text
    return text

def process_file(filename):
    """Process a single JSON file, adding TextENGNew to MSGSET/LOGSET entries."""
    with open(filename, 'r', encoding='utf-8') as f:
        data = json.load(f)

    added = 0
    skipped_exists = 0
    skipped_no_id = 0

    for entry in data:
        entry_type = entry.get('type')
        if entry_type not in ('MSGSET', 'LOGSET'):
            continue

        if 'TextENGNew' in entry:
            skipped_exists += 1
            continue

        msg_id = entry.get('MessageID')
        if msg_id is None:
            skipped_no_id += 1
            continue

        if msg_id in TRANSLATIONS:
            entry['TextENGNew'] = TRANSLATIONS[msg_id]
            added += 1
        else:
            skipped_no_id += 1

    if added > 0:
        with open(filename, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)

    return added, skipped_exists, skipped_no_id

