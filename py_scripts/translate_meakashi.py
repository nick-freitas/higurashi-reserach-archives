def strip_tags(text):
    """Strip game engine tags for reading, but preserve \\n."""
    # Remove @vS... voice tags (up to next whitespace or period)
    text = re.sub(r'@vS[^\s.]*\.', '', text)
    # Remove @w### timing tags
    text = re.sub(r'@w\d+\.', '', text)
    # Remove @k (click wait)
    text = re.sub(r'@k', '', text)
    # Remove @r (line break tag - but preserve \n)
    text = re.sub(r'@r', '', text)
    # Remove @b...@<...@> ruby annotation tags
    text = re.sub(r'@b[^.]*\.@<[^@]*@>', lambda m: re.search(r'@<([^@]*)@>', m.group()).group(1) if re.search(r'@<([^@]*)@>', m.group()) else '', text)
    # Remove @|
    text = re.sub(r'@\|', '', text)
    # Remove `` backtick speech markers
    text = re.sub(r'``', '', text)
    return text.strip()

